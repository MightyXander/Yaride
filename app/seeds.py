def describe_routes_for_help() -> str:
    lines = ["Маршруты (населённый пункт → район → административный район → остановка):"]
    for locality, districts in ROUTE_HIERARCHY.items():
        lines.append(f"\n{locality}:")
        for district, admin_areas in districts.items():
            lines.append(f"  {district}:")
            for admin_area, stops in admin_areas.items():
                lines.append(f"    {admin_area}:")
                for stop in stops:
                    lines.append(f"      - {stop}")
    return "\n".join(lines)
