def _coordinates_from_catalog_file() -> dict[tuple[str, str, str, str], tuple[float, float]]:
    global _COORD_CACHE
    if _COORD_CACHE is None:
        _COORD_CACHE = load_stop_coordinates_catalog()
    return _COORD_CACHE


def _relaxed_index() -> dict[tuple[str, str, str], tuple[float, float]]:
    """Индекс координат по (населённый пункт, район, название) без admin_area.

    В существующих БД admin_area исторически отличается от текущего seeds.py
    (напр. «Земляной город»/«Всполье» вместо «Центр»), из-за чего точный 4-ключевой
    поиск промахивается. Здесь приоритет у ручных override, затем каталог.
    """
    global _RELAXED_CACHE
    if _RELAXED_CACHE is None:
        out: dict[tuple[str, str, str], tuple[float, float]] = {}
        # Каталог кладём первым, override — последним, чтобы override перезаписал.
        for (loc, dist, _a, title), v in _coordinates_from_catalog_file().items():
            out[(loc, dist, title)] = v
        for (loc, dist, _a, title), v in COORDINATE_OVERRIDES.items():
            out[(loc, dist, title)] = v
        _RELAXED_CACHE = out
    return _RELAXED_CACHE


